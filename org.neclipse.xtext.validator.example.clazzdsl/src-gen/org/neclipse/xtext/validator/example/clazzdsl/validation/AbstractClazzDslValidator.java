public abstract class AbstractClazzDslValidator extends AbstractDeclarativeValidator {
	
	@Override
	protected List<EPackage> getEPackages() {
		List<EPackage> result = new ArrayList<EPackage>();
		result.add(org.neclipse.xtext.validator.example.clazzdsl.clazzDsl.ClazzDslPackage.eINSTANCE);
		return result;
	}
	
}
